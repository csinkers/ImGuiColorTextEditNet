namespace ImGuiColorTextEditNet;

/// <summary>Defines the selection modes available.</summary>
public enum SelectionMode
{
    /// <summary>Selects text by characters, allowing for precise selection of individual characters.</summary>
    Normal,

    /// <summary>Selects text by words, allowing for quick selection of entire words at once.</summary>
    Word,

    /// <summary>Selects text by lines, allowing for quick selection of entire lines of text.</summary>
    Line,
}
